package tema01b;

public class Bucles {

    public static void main(String[] args) {
        // for
        for (int i = 0; i < 10; i = i + 1) {
            System.out.println(i + " hola :)");
        }

        // while
        int j = 0;
        while (j < 5) {
            System.out.println("adios :(");
            j++;
        }

        // do-while
        // Enseñar aquí a depurar el programa
        do {
            System.out.println("ciao :(");
        } while (j < 5);
    }
}
